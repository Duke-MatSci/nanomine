<template>
  <div class="SDFReconstruct">
    <h1>{{ msg }}</h1>
    <v-container class="text-xs-left">
      <v-flex xs12>
          <h3>Description</h3>
          <br>
           <p>Upload a image / ZIP file containing set of images (Supported file formats: .jpg, .tif, .png) and click "Reconstruct".
           Reconstructions are generated by level-cutting a Gaussian Random Field (see references)</p>
           <p><strong>The SDF is a function of <italic> spatial </italic> frequency and has the unit 1/pixel.</strong></p>
            <h4> Input Options:</h4>
            <p><strong> NOTE: Images must be binarized.</strong></p>
                  <p><strong> 1) Single image:</strong> (Download <a href='http://129.105.90.149/nm/Two_pt_MCR/Input Samples/Option1.zip'>Sample</a>).
                  Supported image formats are .jpg, .tif and .png. The reconstruction results will include the input and reconstructed images in JPG format, the 2D SDF
                  (data in Input_SDF_2D.csv, plotted in SDF_2D.jpg) and it's radial average (Input_SDF_1D.csv).
                  The reconstructed images will be provided in JPEG format along with a plot comparing their autocorrelation function with the autocorrelation of input image.</p>
                  <p><strong> 2) Single image in .mat format :</strong> The .mat file must contain ONLY ONE variable named "Input" - which contains pixel values(only 0 or 1)
                  (Download <a href='http://129.105.90.149/nm/Two_pt_MCR/Input Samples/Option2.zip'>Sample</a>) The reconstruction results will include the input and reconstructed images
                  in JPG format, the 2D SDF (data in Input_SDF_2D.csv, plotted in SDF_2D.jpg) and it's radial average (Input_SDF_1D.csv).
                  The reconstructed images will be provided in JPEG format along with a plot comparing their autocorrelation function with the mean autocorrelation of input images.</p>
                  <p><strong> 3) Multiple images in ZIP File:</strong> Submit a ZIP file containing multiple images of same size (in pixels).
                  DO NOT ZIP the folder containing images; select all images and ZIP them directly. (Download <a href='http://129.105.90.149/nm/Two_pt_MCR/Input Samples/Option3.zip'>Sample</a>). The mean SDF (averaged over all input images) will be used for reconstruction.
                  The results include 2D and 1D SDF for all input images provided in folders named "Input1", "Input2" etc.
                  The mean SDF of input images are provided in CSV files "SDF_1D_mean.csv" and "SDF_2D_mean.csv" and the 2D mean plotted in "SDF_2D.jpg".
                  The reconstructed images will be provided in JPEG format along with a plot comparing their autocorrelation function with the mean autocorrelation of input images.</p>
      </v-flex>
      <v-alert
        v-model="loginRequired"
        type="error"
        outline
      >
        {{loginRequiredMsg}}
      </v-alert>
      <v-alert
        v-model="errorAlert"
        type="error"
        dismissible
      >
        {{errorAlertMsg}}
      </v-alert>
      <v-dialog v-model="successDlg" persistent max-width="500px">
        <v-card>
          <v-card-title>
            <span>Characterization Job Submitted Successfully</span>
            <v-spacer></v-spacer>
          </v-card-title>
          <v-card-text>
            Your characterization job is: {{jobId}} <br/> You should receive an email with a link to the job output.
          </v-card-text>
          <v-card-actions>
            <v-btn color="primary" flat @click="successDlgClicked()">Close</v-btn>
          </v-card-actions>
        </v-card>
      </v-dialog>
      <v-select label="Number Of Reconstructions" v-bind:items="options" v-model="num_recon"></v-select>
      <ImageUpload v-on:setFiles="setFiles"></ImageUpload>
      <v-flex class="text-xs-center">
      <v-btn v-on:click="submit()" color="primary">Reconstruct</v-btn>
      </v-flex>
      <h4>References</h4>
      <v-flex xs12>
        <p>Ghumman, U.F., Iyer, A., Dulal, R., Munshi, J., Wang, A., Chien, T., Balasubramanian, G., and Chen, W., A Spectral Density Function Approach for Active Layer Design of Organic Photovoltaic Cells, <i>Journal of Mechanical Design</i>, Special Issue on Design of Engineered Materials and Structures, accepted July 2018. doi:10.1115/1.4040912.</p>
        <p>Yu, S., Zhang, Y., Wang, C., Lee, W.K., Dong, B., Odom, T.W., Sun, C. and Chen, W., 2017. Characterization and design of functional quasi-random nanostructured materials using spectral density function. <i>Journal of Mechanical Design</i>, 139(7), p.071401.</p>
        <p>Lee, W. K., Yu, S., Engel, C. J., Reese, T., Rhee, D., Chen, W., & Odom, T. W. (2017). Concurrent design of quasi-random photonic nanostructures. Proceedings of the National Academy of Sciences, 114(33), 8734-8739.</p>
      </v-flex>
    </v-container>
  </div>
</template>

<script>
import {} from 'vuex'
import {JobMgr} from '@/modules/JobMgr.js'
import {Auth} from '@/modules/Auth.js'
import ImageUpload from './ImageUpload.vue'

export default {
  name: 'SDFReconstruct',
  data: () => {
    return ({
      title: 'Input Upload',
      msg: 'Microstructure Reconstruction - Spectral Density Function',
      dialog: false,
      fileName: '',
      files: [],
      errorAlert: false,
      errorAlertMsg: '',
      loginRequired: false,
      loginRequiredMsg: '',
      successDlg: false,
      jobId: '',
      num_recon: null,
      options: [
        '1', '2', '3', '4', '5'
      ]
    })
  },
  beforeMount: function () {
    let vm = this
    vm.auth = new Auth()
    if (!vm.auth.isLoggedIn()) {
      vm.loginRequired = true
      vm.loginRequiredMsg = 'Login is required.'
    }
  },
  methods: {

    setFiles: function (...files) {
      this.files = files[0]; // the actual file object
      this.fileName = files[1]; // the name of the file
    },

    setLoading: function () {
      this.$store.commit('isLoading')
    },

    resetLoading: function () {
      this.$store.commit('notLoading')
    },

    successDlgClicked: function () {
      let vm = this
      console.log('Success dlg button clicked')
      vm.$router.go(-2) // go back to mcr homepage page
    },
    submit: function () {
      let vm = this
      vm.files.forEach(function (v) {
        console.log(JSON.stringify(v))
      })

      vm.setLoading()
      console.log('Loading..')
      let jm = new JobMgr()
      console.log('Called Job Manager')
      jm.setJobType('SDFReconstruct')
      jm.setJobParameters({'NumOfReconstructs': vm.num_recon, 'InputType': vm.fileName.split('.').pop()}) // Figure out which input type
      if (vm.files && vm.files.length >= 1) {
        vm.files.forEach(function (v) {
          jm.addInputFile(v.fileName, v.fileUrl)
          console.log('Job Manager added file: ' + v.fileName)
        })
        return jm.submitJob(function (jobId) {
          console.log('Success! JobId is: ' + jobId)
          vm.jobId = jobId
          vm.resetLoading()
          vm.successDlg = true
        }, function (errCode, errMsg) {
          let msg = 'error: ' + errCode + ' msg: ' + errMsg
          console.log(msg)
          vm.errorAlertMsg = msg
          vm.errorAlert = true
          vm.resetLoading()
        })
      } else {
        let msg = 'Please select a file to process.'
        vm.errorAlertMsg = msg
        vm.errorAlert = true
        vm.resetLoading()
      }
    }
  }
}
</script>

